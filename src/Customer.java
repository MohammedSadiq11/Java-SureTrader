 // to get list of all users from mysql database
    public ArrayList<CustLink> getCustLink()
    {
        ArrayList<CustLink> custlink = new ArrayList<CustLink>();
       Connection conn = getConnection();
       
       String query = "SELECT * FROM importexport.customer ";
       Statement st;
       ResultSet rs;
       
       try {
           st = conn.createStatement();
           rs = st.executeQuery(query);

           CustLink cust;

           while(rs.next())
           {
                cust = new CustLink(rs.getInt("customer_id"),rs.getString("customer_name"),rs.getString("gender"),rs.getString("business_type"),rs.getInt("contact_no"),rs.getString("address"),rs.getString("date"))  ;
                custlink.add(cust);
    }
       }catch(Exception e){
           e.printStackTrace();
       }
        return custlink;   
       }
    
