    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
         String path="";
        JFileChooser j=new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x=j.showSaveDialog(this);
        
        if(x==JFileChooser.APPROVE_OPTION)
        {
            path=j.getSelectedFile().getPath();
        }
        Document doc=new Document();
        try {
            try {
                PdfWriter.getInstance(doc,new FileOutputStream(path+"Customer_details.pdf"));
                doc.open();
                doc.add(new Paragraph("IMPORT-EXPORT MANAGEMENT SYSTEM",FontFactory.getFont(FontFactory.TIMES_BOLD)));
                //import util.Date package
                doc.add(new Paragraph(new Date().toString()));
                doc.add(new Paragraph("---------------------------------------------------------"));
                PdfPTable tb1=new PdfPTable(8);
                
                PdfPCell cell=new PdfPCell(new Paragraph("SALES DETAILS"));
                cell.setColspan(4);
                cell.setHorizontalAlignment(Element.ALIGN_CENTER);
               // cell.setBackgroundColor(BaseColor.BLUE);
                
                tb1.addCell(cell);
                
                tb1.addCell("Customer ID");
                tb1.addCell("Customer Name");
                tb1.addCell("Gender");
                tb1.addCell("Business Type");
                tb1.addCell("Contact");
                tb1.addCell("Address");
                tb1.addCell("Date");
                 for(int i=0;i<jTable_display.getRowCount();i++){
                     String c_id=jTable_display.getValueAt(i,0).toString();
                     String c_name=jTable_display.getValueAt(i,1).toString();
                     String gender=jTable_display.getValueAt(i,2).toString();
                     String b_type=jTable_display.getValueAt(i,3).toString();
                      String contact=jTable_display.getValueAt(i,4).toString();
                       String address=jTable_display.getValueAt(i,5).toString();
                        String date=jTable_display.getValueAt(i,6).toString();
                     
                     tb1.addCell(c_id);
                     tb1.addCell(c_name);
                     tb1.addCell(gender);
                      tb1.addCell(b_type);
                       tb1.addCell(contact);
                     tb1.addCell(address);
                      tb1.addCell(date);
                     
                     
                     
                     
                 }
                 doc.add(tb1);
                 JOptionPane.showMessageDialog(null,"saved successfully :)", "ALERT MESSAGE", JOptionPane.OK_OPTION);
                
                
                
            } catch (DocumentException ex) {
                Logger.getLogger(BillingDetails.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(BillingDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
        doc.close();
        
    }//GEN-LAST:event_jButton9ActionPerformed
        
           
         
