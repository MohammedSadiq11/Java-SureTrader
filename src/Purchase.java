 // to get list of all users from mysql database
    public ArrayList<PurLink> getPurchLink()
    {
        ArrayList<PurLink> purlink = new ArrayList<PurLink>();
       Connection conn = getConnection();
       
       String query = "SELECT * FROM importexport.purchase ";
       Statement st;
       ResultSet rs;
       
       try {
           st = conn.createStatement();
           rs = st.executeQuery(query);

           PurLink purch;

           while(rs.next())
           {
                purch = new PurLink(rs.getInt("seller_id"),rs.getString("seller_name"),rs.getInt("product_id"),rs.getString("product_name"),rs.getInt("product_price"),rs.getString("date")) ;
                purlink.add(purch);
    }
       }catch(Exception e){
           e.printStackTrace();
       }
        return purlink;   
       }
    
